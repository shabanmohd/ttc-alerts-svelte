/**
 * Get the display name for a route
 * @param routeId - The route ID (e.g., "130", "Line 1", "501")
 * @returns The route name or empty string if not found
 */
export function getRouteName(routeId: string): string {
  return ROUTE_NAMES[routeId] || "";
}

/**
 * Get the full display string for a route (number + name)
 * @param routeId - The route ID (e.g., "130", "Line 1", "501")
 * @returns Full display string (e.g., "130 Middlefield") or just the ID if name not found
 */
export function getRouteDisplayName(routeId: string): string {
  const name = ROUTE_NAMES[routeId];
  if (!name) return routeId;

  // For subway lines, just return the name (badge shows "Line X")
  if (routeId.startsWith("Line")) {
    return name;
  }

  // For bus/streetcar routes, return "number name"
  return `${routeId} ${name}`;
}
